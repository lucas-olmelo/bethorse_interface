    public long pegarCpf(){
        ConexaoBD cbd = new ConexaoBD();
        Connection con = cbd.connectionMySql();
        
        long cpf = 0;
        try{
            String query = "SELECT cpf FROM usuario WHERE email = ?;";
            PreparedStatement stat = con.prepareStatement(query);
            stat.setString(1, email);
            ResultSet result  = stat.executeQuery();
            while (result.next()){
                cpf = result.getLong("cpf");
            } 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro!!\n" + e, "Erro", JOptionPane.ERROR_MESSAGE);
        }
        return cpf;
    }
    
    public String nomeDn(){
        
        String nome = "";
        try{
            String sql = "SELECT nome FROM usuario WHERE cpf = " +pegarCpf();
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
                nome = rs.getString("nome");
            }
        } catch (SQLException e){
            System.out.println(e);
        }
        return nome;
    }
    
