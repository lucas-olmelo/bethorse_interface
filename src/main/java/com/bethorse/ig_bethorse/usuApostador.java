    public long pegarCpf(){
        ConexaoBD cbd = new ConexaoBD();
        Connection con = cbd.connectionMySql();
        
        long cpf = 0;
        try{
            String query = "SELECT cpf FROM usuario WHERE email = ?;";
            PreparedStatement stat = con.prepareStatement(query);
            stat.setString(1, email);
            ResultSet result  = stat.executeQuery();
            while (result.next()){
                cpf = result.getLong("cpf");
            } 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro!!\n" + e, "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        return cpf;
    }
    
    public String nomeAp(){
        
        String nome = "";
        try{
            String sql = "SELECT nome FROM usuario WHERE cpf = " +pegarCpf();
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()){
                nome = rs.getString("nome");
            }
        } catch (SQLException e){
            System.out.println(e);
        }
        return nome;
    }
    
    public ArrayList mostrarDadosAp(){
        ArrayList dados = new ArrayList();
        try{
            String sql = "SELECT * FROM usuario WHERE cpf = ?;";
            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setLong(1, pegarCpf());
            ResultSet rs = pstm.executeQuery();
            while(rs.next()){
                dados.add(rs.getLong("cpf"));
                dados.add(rs.getString("nome"));
                dados.add(rs.getString("senha"));
                dados.add(rs.getString("email"));
                dados.add(rs.getString("telefone"));
            }
        }catch(Exception e){
            System.out.println(e);
        }
        return dados;
    }
    
