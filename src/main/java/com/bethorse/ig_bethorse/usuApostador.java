    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Aposta ap = new Aposta();
        usuDono dn = new usuDono();
        
        String cavalo = selectCavalo.getSelectedItem().toString();
        String nome = dn.repararString(cavalo)[0];
        String dados = ap.getDados(nome);
        
        float odd = ap.calcularOdds(dados);
        JOptionPane.showMessageDialog(null, "Valor da Odd: " + df.format(odd));
        float valor = Float.parseFloat(txtvalor.getText());
        
        int op = JOptionPane.showConfirmDialog(null, "Você poderá ganhar " +df.format(valor*odd) + " com sua aposta de " + df.format(valor) + ".\nDeseja apostar?", "Aposta", JOptionPane.YES_NO_OPTION);
        if (op == 0){
            AddCred cr = new AddCred();
            if (cr.pegarSaldo(con, email) >= valor){
                cr.Remover(con, email, valor);
                String corrida = selectCorrida.getSelectedItem().toString();
                ap.criarAposta(pegarCpf(), valor, corrida, nome);
                jLabel2.setText(String.valueOf(df.format(cr.pegarSaldo(con, email))));
            } else {
                JOptionPane.showMessageDialog(null, "Saldo insuficiente! Adicione saldo na página inicial", "Sem créditos", JOptionPane.ERROR_MESSAGE);
            }
            jLabel20.setText(df.format(valor*odd));
        }
        selectCor();
        selectCav();
        txtvalor.setText("");
    }//GEN-LAST:event_jButton4ActionPerformed

